    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(curso == null){
            curso = new Curso();
        }
        Date data = new Date();
        List<Aula> aulas = new ArrayList<>();
        curso.setInstrutor((Instrutor) cmbInstrutor.getSelectedItem());
        curso.setAluno((Aluno) cmbAluno.getSelectedItem());
        curso.setCategoria((Categoria) cmbCategoria.getSelectedItem());
        if(curso.getAdministrativo() == null){
            curso.setAdministrativo(adm);
        }
        if(curso.getData_inicio() == null){
            curso.setData_inicio(data);
        }
        if((Categoria) cmbCategoria.getSelectedItem() == Categoria.A || (Categoria) cmbCategoria.getSelectedItem() == Categoria.B){
            curso.setCarga_horaria(20);
        }else{
            curso.setCarga_horaria(15);
        }
        if(curso.getAulas() == null){
            curso.setAulas(aulas);
        }
        jpa.conexaoAberta();
        try{
            jpa.persist(curso);
        }catch(Exception e){
            System.out.println("Erro encontrado: " + e);
        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

