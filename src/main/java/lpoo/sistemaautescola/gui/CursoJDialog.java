    private void carregaCmbs() {
        carregaCmbAluno();
        carregaCmbInstrutor();
        carregaCmbCategoria();
    }
    
    private void carregaCmbInstrutor(){
        jpa.conexaoAberta();
        try{
            List<Instrutor> listaInstrutores = jpa.getInstrutores();
            cmbInstrutor.removeAllItems();
            cmbInstrutor.addItem(null);
            for(Instrutor i : listaInstrutores){
                cmbInstrutor.addItem(i);
            }
        }catch(Exception e){
            System.out.println("Erro encontrado: "+e);
        }
        jpa.fecharConexao();
    }
    
    private void carregaCmbAluno(){
        jpa.conexaoAberta();
        try{
            List<Aluno> listaAlunos = jpa.getAlunos();
            cmbAluno.removeAllItems();
            cmbAluno.addItem(null);
            for(Aluno a : listaAlunos){
                cmbAluno.addItem(a);
            }
        }catch(Exception e){
            System.out.println("Erro encontrado: "+e);
        }
        jpa.fecharConexao();
    }
    
    private void carregaCmbCategoria(){
        cmbCategoria.removeAllItems();
        for(Categoria c : Categoria.values()){
            cmbCategoria.addItem(c);
        }
    }
