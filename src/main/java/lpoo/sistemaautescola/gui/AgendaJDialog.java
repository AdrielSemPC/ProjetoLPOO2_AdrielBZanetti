    public void carregaCmb(){
        String[] hora = new String[24];
        cmbHora.removeAllItems();
        cmbHora.addItem(null);
        for (int i = 7; i < 12; i++) {
            hora[i] = String.format("%02d:30", i);
            cmbHora.addItem(hora[i]);
        }
        for (int i = 14; i < 20; i++) {
            hora[i] = String.format("%02d:30", i);
            cmbHora.addItem(hora[i]);
        }
    }

