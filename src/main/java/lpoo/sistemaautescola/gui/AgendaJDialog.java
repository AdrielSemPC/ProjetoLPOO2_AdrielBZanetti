    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        Date novaData = new Date();
        String data = txtData.getText();
        String hora = (String)cmbHora.getSelectedItem();
        String dataHora = data+" "+hora;
        try {
            novaData = f.parse(dataHora);
            Aula auxAula = new Aula();
            auxAula.setDia_aula(novaData);
            auxAula.setConcluida(false);
            auxAula.setCurso(c);
            c.addAula(auxAula);
            try{
                jpa.persist(c);
            }catch(Exception e){
                System.out.println("Erro :"+e);
            }
        } catch (ParseException ex) {
            Logger.getLogger(AgendaJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

